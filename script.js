function renderCards(array) {
  let grid = document.querySelector('.grid');
  while (grid.firstChild) {
    grid.firstChild.remove();
  };
  for (let i = 0; i < array.length; i++) {
    let cardToRender = generateCard(array[i]);
    grid.appendChild(cardToRender);
  };
};

function displayCardCount(array) {
  let count = array.length;
  let max = cards.length;
  let message = `Displaying ${count} / ${max} cards`;
  countDisplay.innerText = message;
}

function filterCards(array) {
  // define rank filter
  let rankFilter = false;
  let filterForStandard = standardFilter.checked;
  let filterForRankOneFeats = rankOneFeatsFilter.checked;

  // define weapon filter
  let weaponFilter = false;
  let filterForAnyWeapon = anyWeaponFilter.checked;
  let filterForNonWeapon = nonWeaponFilter.checked;
  let filterForArcing = arcingFilter.checked;
  let filterForBashing = bashingFilter.checked;
  let filterForThrusting = thrustingFilter.checked;
  let filterForBulwark = bulwarkFilter.checked;
  let filterForMomentous = momentousFilter.checked;
  let filterForPhoton = photonFilter.checked;
  let filterForSonic = sonicFilter.checked;
  let filterForThermal = thermalFilter.checked;
  let filterForExplosive = explosiveFilter.checked;
  let filterForMelee = meleeFilter.checked;
  let filterForRanged = rangedFilter.checked;
  let filterForOneHand = oneHandFilter.checked;
  let filterForTwoHand = twoHandFilter.checked;

  let filteredArray = array.filter((card) => {
    // check rank filter
    if (!filterForStandard && !filterForRankOneFeats) {
      rankFilter = true;
    } else if (filterForStandard && card.system.traits.includes('Standard')) {
      rankFilter = true;
    } else if (filterForRankOneFeats && card.system.traits.includes('Rank 1 Feat')) {
      rankFilter = true;
    } else {
      return false;
    };
    // check weapon filter
    if (!filterForAnyWeapon && !filterForNonWeapon && !filterForArcing && !filterForBashing && !filterForThrusting && !filterForBulwark
          && !filterForMomentous && !filterForPhoton && !filterForSonic && !filterForThermal && !filterForExplosive && !filterForMelee
              && !filterForRanged && !filterForOneHand && !filterForTwoHand) {
      weaponFilter = true;
    } else if (filterForAnyWeapon && card.system.tags && card.system.tags.includes('Any Weapon')) {
      weaponFilter = true;
    } else if (filterForNonWeapon && card.system.tags && card.system.tags.includes('Non Weapon')) {
      weaponFilter = true;
    } else if (filterForArcing && card.system.tags && card.system.tags.includes('Arcing')) {
      weaponFilter = true;
    } else if (filterForBashing && card.system.tags && card.system.tags.includes('Bashing')) {
      weaponFilter = true;
    } else if (filterForThrusting && card.system.tags && card.system.tags.includes('Thrusting')) {
      weaponFilter = true;
    } else if (filterForBulwark && card.system.tags && card.system.tags.includes('Bulwark')) {
      weaponFilter = true;
    } else if (filterForPhoton && card.system.tags && card.system.tags.includes('Photon')) {
      weaponFilter = true;
    } else if (filterForSonic && card.system.tags && card.system.tags.includes('Sonic')) {
      weaponFilter = true;
    } else if (filterForThermal && card.system.tags && card.system.tags.includes('Thermal')) {
      weaponFilter = true;
    } else if (filterForExplosive && card.system.tags && card.system.tags.includes('Explosive')) {
      weaponFilter = true;
    } else if (filterForMelee && card.system.tags && card.system.tags.includes('Melee')) {
      weaponFilter = true;
    } else if (filterForRanged && card.system.tags && card.system.tags.includes('Ranged')) {
      weaponFilter = true;
    } else if (filterForOneHand && card.system.tags && card.system.tags.includes('1H')) {
      weaponFilter = true;
    } else if (filterForTwoHand && card.system.tags && card.system.tags.includes('2H')) {
      weaponFilter = true;
    } else {
      return false;
    };
    // check if all filters are met (and logic)
    return rankFilter && weaponFilter;
  });
  return filteredArray;
};

function renderBasedOnFilters(array) {
  let filteredArray = filterCards(array);
  displayCardCount(filteredArray);
  renderCards(filteredArray);
};

function generateCard(card) {
  // card container
  let cardContainer = document.createElement('div');
  cardContainer.classList.add('individualCard');

  // card header
  let cardHeader = document.createElement('div');
  cardHeader.classList.add('cardHeader');
  cardContainer.appendChild(cardHeader);
  let cardName = document.createElement('div');
  cardName.innerText = card.name;
  cardHeader.appendChild(cardName);
  let cardCost = document.createElement('div');
  cardCost.innerText = card.system.tempoCost;
  cardHeader.appendChild(cardCost);

  // card body
  let cardBody = document.createElement('div');
  cardBody.classList.add('cardBody');
  cardContainer.appendChild(cardBody);
  let cardTraits = document.createElement('div');
  cardTraits.innerText = card.system.traits;
  cardBody.appendChild(cardTraits);
  let cardDescription = document.createElement('div');
  cardDescription.innerText = card.system.description;
  cardBody.appendChild(cardDescription);
  if (card.system.keyword) {
    let cardKeyword = document.createElement('div');
    cardKeyword.innerText = card.system.keyword;
    cardBody.appendChild(cardKeyword);
  };

  // render card
  return cardContainer;
};


